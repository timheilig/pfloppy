class Encoding(object):
    def __init__(self):
        self._entries = []

    @staticmethod
    def is_font_specific():
        """
        Determines if the encoding is font specific or not.
        :rtype: bool
        """
        return False

    def get_code(self, sid):
        """
        Returns the code corresponding to the given SID, or -1 if SID doesn't exist
        :param int sid:
        :rtype: int
        """
        for entry in self._entries:
            if entry.sid == sid:
                return entry.code
        return -1

    def get_sid(self, code):
        """
        Returns the SID corresponding to the given code, or -1 if code doesn't exist
        :param int code:
        :rtype: int
        """
        for entry in self._entries:
            if entry.code == code:
                return entry.sid
        return -1

    def register(self, code, sid):
        """
        Adds a new code/SID combination to the encoding.

        :param int code:
        :param int sid:
        """
        self._entries.append(Entry(code, sid))


class Entry(object):
    """
    This class represents a single code/SID mapping of the encoding.
    """
    def __init__(self, code, sid):
        self.code = code
        self.sid = sid

    def __repr__(self):
        return "[code={0}, sid={1}]".format(self.code, self.sid)


